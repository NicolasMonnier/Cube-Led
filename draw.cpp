#include "draw.hpp"

void init_IO(){
	/* Bus 8 bits*/
	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	/*Layers control - 8 bits*/
	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);


	/* 3 bits select bus*/
	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);

	pinMode(1,OUTPUT);
	digitalWrite(1,0);


	/*Enable*/
	pinMode(1,OUTPUT);
	digitalWrite(1,0);



}


